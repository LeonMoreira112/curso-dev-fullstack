
let idade = 18

if (idade >= 18) {
    document.write ('Ok, você é maior de idade') 
    // Se idade >= 18 a mensagem que aparece é "Ok, você é maior de idade"
} else if (idade === 15) {
    document.write ('Você tem 15 anos!') 
    // Se idade = 15 anos a mensagem que aparece é "Você tem 15 anos!"
} else {
    document.write ( 'Você não pode acessar') 
    // Se idade < 18 e diferente de 15 a mensagem que aparece é "Você não pode acessar"
}

// O código é lido de cima para baixo, a condição que condizer, o código será executado.

// If é uma condição (Se)
// Else é outra condição (Senão)
// Else if (Senão + Se) condição de uma condição
