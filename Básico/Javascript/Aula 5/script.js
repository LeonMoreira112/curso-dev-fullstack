/*
Operadores aritméticos:

adição: +
subtração: -
multiplicação: *
divisão: /
incremento: ++
decremento: --

*/

// adiçaõ
let a = 10
let b = 5 // Caso fosse indefinido os resultados dariam NaN (Not a number)
let c = a + b // O sinal de + também serve para concatenar
console.log(c) // C = a + b / c = 15
console.log(a + b) // a + b = 15

// subtração
let d = 10
let e = 5
let f = d - e
console.log(f) // f = d - e / f = 5
console.log(d - e) // d - e = 5

// multiplicação
let g = 10
let h = 5
let i = g * h
console.log(i) // i = g * h / i = 50
console.log(g * h) // g * h = 50

// divisão
let j = 10
let k = 5
let l = j / k
console.log(l) // l = j / k / l = 2
console.log(j / k) // j / k = 2

// incremento 
let m = 10
m++
console.log(m) // O incremento aumenta a variável em 1 unidade = 11

// decremento
let n = 10
n--
console.log(n) // O decremento diminui a variável em 1 unidade = 9

// atribuiçaõ +
let o = 10
o += 25 // o = o + 25
console.log(o) // Resultado = 10 + 25 = 35

// atribuição -
let p = 10
p -= 25 // p = p - 25
console.log(p) // Resultado 10 - 25 = -15